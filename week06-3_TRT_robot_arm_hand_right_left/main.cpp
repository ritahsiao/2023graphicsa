///week06-3_TRT_robot_arm_hand_right_left///拿上週的week05-2來用
///畫出大的身體,畫出手臂
#include <GL/glut.h>
float angle=0;
void drawHand()
{
    glPushMatrix();
        glScalef(1,0.3,0.3);
        glutSolidCube(0.5);
    glPopMatrix();
}
void display()
{
    glClear( GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

    glPushMatrix();///右上手臂
        glTranslatef(0.25,0,0);///(3)要掛上去
        glRotatef(angle,0,0,1);///(2)轉動
        glTranslatef(0.25,0,0);///(1)把中心放在中心
        ///step03-1 把前面的T-R-T都先註解掉,會較好理解
        ///設定好後,再把上面的程式解開來
        drawHand();///右上手臂

        glPushMatrix();///右下手肘
            glTranslatef(0.25,0,0);///(3)要掛上去
            glRotatef(angle,0,0,1);///(2)轉動
            glTranslatef(0.25,0,0);///(1)把中心放在中心
            drawHand();///右下手肘
        glPopMatrix();
    glPopMatrix();

    glPushMatrix();///左上手臂
        glTranslatef(-0.25,0,0);///(3)要掛上去
        glRotatef(angle,0,0,1);///(2)轉動
        glTranslatef(-0.25,0,0);///(1)把中心放在中心
        ///step03-1 把前面的T-R-T都先註解掉,會較好理解
        ///設定好後,再把上面的程式解開來
        drawHand();///左上手臂

        glPushMatrix();///左下手肘
            glTranslatef(-0.25,0,0);///(3)要掛上去
            glRotatef(angle,0,0,1);///(2)轉動
            glTranslatef(-0.25,0,0);///(1)把中心放在中心
            drawHand();///左下手肘
        glPopMatrix();
    glPopMatrix();

    glutSwapBuffers();
    angle++;
}
int main(int argc, char *argv[])
{
    glutInit(&argc, argv);
    glutInitDisplayMode(GLUT_RGB | GLUT_DOUBLE | GLUT_DEPTH);
    glutCreateWindow("week06");

    glutDisplayFunc(display);
    glutIdleFunc(display);
    glutMainLoop();
}
